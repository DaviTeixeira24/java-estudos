package AprendendoMetodos;

public class MetodosBuiltInMath {
  public static void main(String[] args) {
    // 11 - funcoes built in matem√°ticas(Math)

    // Raiz quadrada
    System.out.println(Math.sqrt(14));
    System.out.println(Math.sqrt(9));

    // Potencia
    System.out.println(Math.pow(3, 5));

    // valor asoluto
    System.out.println(Math.abs(-11));

    // Maior entre dois numeros
    System.out.println(Math.max(11.90, 11.89));

    // encapsulando

    double raizQuadrada = Math.sqrt(99);
    System.out.println(raizQuadrada);

  }
}
